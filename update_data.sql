CREATE PROCEDURE SuaKM_CB
    @SH_DOT VARCHAR(20),
    @SH_CB VARCHAR(20),
    @GIA_VE_KM DECIMAL(10, 2)
AS
BEGIN
    UPDATE KM_CB
    SET GIA_VE_KM = @GIA_VE_KM
    WHERE SH_DOT = @SH_DOT AND SH_CB = @SH_CB
END;
GO

