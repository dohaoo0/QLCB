CREATE PROCEDURE SuaTuyenBay
    @MTB VARCHAR(20),
    @TP1 VARCHAR(50),
    @TP2 VARCHAR(50)
AS
BEGIN
    UPDATE TUYENBAY
    SET TP1 = @TP1,
        TP2 = @TP2
    WHERE MTB = @MTB
END;
GO

CREATE PROCEDURE SuaChuyenBay
    @SH_CB VARCHAR(20),
    @GIO_BAY DATETIME,
    @GIO_DEN DATETIME,
    @GIA_VE DECIMAL(10, 2),
    @GG FLOAT,
    @LG_VE_MAX INT,
    @MTB VARCHAR(20)
AS
BEGIN
    UPDATE CHUYENBAY
    SET GIO_BAY = @GIO_BAY,
        GIO_DEN = @GIO_DEN,
        GIA_VE = @GIA_VE,
        GG = @GG,
        LG_VE_MAX = @LG_VE_MAX,
        MTB = @MTB
    WHERE SH_CB = @SH_CB
END;
GO

