CREATE PROCEDURE XoaTuyenBay
    @MTB VARCHAR(20)
AS
BEGIN
    DELETE FROM TUYENBAY
    WHERE MTB = @MTB
END;
GO

CREATE PROCEDURE XoaChuyenBay
    @SH_CB VARCHAR(20)
AS
BEGIN
    DELETE FROM CHUYENBAY
    WHERE SH_CB = @SH_CB
END;
GO

CREATE PROCEDURE XoaThucHien
    @SH_CB VARCHAR(20)
AS
BEGIN
    DELETE FROM THUCHIEN
    WHERE SH_CB = @SH_CB
END;
GO

CREATE PROCEDURE XoaKM
    @SH_DOT VARCHAR(20)
AS
BEGIN
    DELETE FROM KM
    WHERE SH_DOT = @SH_DOT
END;
GO

CREATE PROCEDURE XoaKM_CB
    @SH_DOT VARCHAR(20),
    @SH_CB VARCHAR(20)
AS
BEGIN
    DELETE FROM KM_CB
    WHERE SH_DOT = @SH_DOT AND SH_CB = @SH_CB
END; 
GO

